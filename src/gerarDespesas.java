    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(cust.size() == 0){
            errorTextField.setText("Adicionar pelo menos uma despesa.");
        }
        else{
            Estoque.allFinances.get(Estoque.allFinances.size()-1).custos.addAll(cust);
            float custoTotal = 0.0f;
            for(finances.custoItems cI : Estoque.allFinances.get(Estoque.allFinances.size()-1).custos){
                custoTotal += cI.valor;
            }
            Estoque.allFinances.get(Estoque.allFinances.size()-1).valorCustos = custoTotal;
            financeiro.gastosLabel.setText("R$"+String.valueOf(custoTotal));
            this.dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

