    private String filterVal(String val){
        if(val.indexOf(",") != -1 && val.indexOf(".") != -1){
            val = val.substring(0,val.indexOf('.'))+val.substring(val.indexOf('.')+1);
            val = val.replace(',', '.');
        }
        else if(val.indexOf(",") != -1){
            val = val.replace(',', '.');
        }
        return val;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        String nome, data;
        float valor = 0.0f;
        nome = despesaTextField.getText();
        data = dataTextField.getText();
        finances f = new finances();
        String val = valorTextField.getText();
        val = filterVal(val);
        
        try{
            valor = Float.parseFloat(val);
            finances.custoItems temp = f.new custoItems(nome,data,valor);
            cust.add(temp);
        }
        catch (Exception e){
            errorTextField.setText("Favor checar as informa√ß√µes acima.");
        }
        despesaTextField.setText("");
        dataTextField.setText("");
        valorTextField.setText("");
        totalDespesasTextField.setText(String.valueOf(cust.size()));
        }catch(Exception e){
            errorTextField.setText(String.valueOf(e));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

