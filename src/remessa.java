    private void produtosExistentesComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_produtosExistentesComboBoxActionPerformed
        // TODO add your handling code here:
        String produto = produtosExistentesComboBox.getItemAt(produtosExistentesComboBox.getSelectedIndex());
        if(!produto.equals("-")){
            produtos.produto prod = null;
            for(produtos.produto p:Estoque.allProdArray){
                if(produto.contains(p.nome) || p.nome.contains(produto)){
                    prod = p;
                    break;
                }
            }
            int i;

            nameTextField.setText(prod.nome);
            descTextField.setText(prod.desc);

            for(i = 0;i<departmentComboBox.getItemCount();i++){
                if(departmentComboBox.getItemAt(i).equals(prod.dept)) break;
            }
            departmentComboBox.setSelectedIndex(i);

            for(i = 0;i<storeComboBox.getItemCount();i++){
                if(storeComboBox.getItemAt(i).equals(prod.loja)) break;
            }
            storeComboBox.setSelectedIndex(i);

            for(i = 0;i<sexoComboBox.getItemCount();i++){
                if(sexoComboBox.getItemAt(i).equals(prod.sexo)) break;
            }
            sexoComboBox.setSelectedIndex(i);

            for(i = 0;i<sizeComboBox.getItemCount();i++){
                if(sizeComboBox.getItemAt(i).equals(prod.tamanho)) break;
            }
            sizeComboBox.setSelectedIndex(i);

            //valorFormattedTextField.setText(String.valueOf(prod.valor.valor_final));
            valorFormattedTextField.setText(String.valueOf(prod.valor.valor_item));
        }
        else{
            clearAllFields();
        }
    }//GEN-LAST:event_produtosExistentesComboBoxActionPerformed

    private void salvarRemessaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarRemessaButtonActionPerformed
        // TODO add your handling code here:
        try{
        produtos p = new produtos();
        produtos.remessa novoPct = p.new remessa();
        float total = 0.0f;
        for(produtos.produto s: tempProds){
            total+=Float.parseFloat(filterVal(String.valueOf(s.valor.valor_item)));
        }
        novoPct.data = data;
        novoPct.id = Estoque.totalPacotes+1;
        novoPct.dolar = dolar;
        novoPct.total_dolar = total;
        novoPct.total_real = total*dolar;
        novoPct.imposto = imposto;
        novoPct.frete = frete;
        
        
        for(produtos.produto s:tempProds){
            s.valor.porcentagem_do_total = s.valor.valor_item_real/novoPct.total_real; 
            s.valor.transporte = novoPct.frete*s.valor.porcentagem_do_total;
            s.valor.imposto = novoPct.imposto*s.valor.porcentagem_do_total;
            s.valor.valor_item_real = s.valor.valor_item*dolar;
            s.valor.imposto = imposto*s.valor.porcentagem_do_total;
            if(s.valor.valor_final == 0.0f){
                s.valor.valor_final = s.valor.imposto+s.valor.transporte+s.valor.valor_item_real;
                s.valor.valor_final += (s.valor.valor_final*0.8);
            }
            s.valor.lucro = s.valor.valor_item_real + s.valor.transporte + s.valor.imposto;
        }
        
        Estoque.pacotes.add(novoPct);
        Estoque.allProdArray.addAll(tempProds);
        
        Estoque.totalPacotes++;
        Estoque.MSGTextField.setText(String.valueOf(Estoque.totalPacotes));
        Estoque.reCalculateTotalProd();
        this.dispose();
        }catch(Exception e){
            errorTextField.setText(String.valueOf(String.valueOf(e)));
        }
    }//GEN-LAST:event_salvarRemessaButtonActionPerformed

