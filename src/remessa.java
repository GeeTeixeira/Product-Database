    private String filterVal(String val){
        if(val.indexOf(",") != -1 && val.indexOf(".") != -1){
            val = val.substring(0,val.indexOf('.'))+val.substring(val.indexOf('.')+1);
            val = val.replace(',', '.');
        }
        else if(val.indexOf(",") != -1){
            val = val.replace(',', '.');
        }
        return val;
    }
    
    private void proxProdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proxProdButtonActionPerformed
        // TODO add your handling code here:
        try{
        String val = valorFormattedTextField.getText(); // valor de compra em dolares
        val = filterVal(val);
        String val2 = vendaFormattedTextField.getText(); // valor final para vender
        val2 = filterVal(val2);
        String nome = String.valueOf(nameTextField.getText());
        String desc = String.valueOf(descTextField.getText());
        String dpt = departmentComboBox.getItemAt(departmentComboBox.getSelectedIndex());
        String loja = storeComboBox.getItemAt(storeComboBox.getSelectedIndex());
        String gender =  sexoComboBox.getItemAt(sexoComboBox.getSelectedIndex());
        String size = sizeComboBox.getItemAt(sizeComboBox.getSelectedIndex());
        float valor = Float.parseFloat(val); //valor de compra em dolares
        float venda = Float.parseFloat(val2); // valor final para vender
        int qnt = (int) qntSpinner.getValue();
        if(nome.equals("") || desc.equals("") || dpt.equals("-") || 
                size.equals("-") || valor == 0.00f || loja.equals("-")){
                errorTextField.setText("Confirme que todos os campos de informação estajam preenchidos.");
        }
        else{
            produtos prod = new produtos();
            produtos.produto temp = prod.new produto(nome,desc,loja,dpt,size,qnt,gender,Estoque.totalPacotes+1);
            temp.valor = prod.new precos(valor,dolar,venda);

            tempProds.add(temp);
            prodNames.add(nome);
            
            counter++;
            counterTextField.setText(String.valueOf(counter));
            errorTextField.setText(String.valueOf(""));
            
            clearAllFields();
        }
        }catch(Exception e){
            errorTextField.setText(String.valueOf(String.valueOf(e)));
        }
        
    }//GEN-LAST:event_proxProdButtonActionPerformed

